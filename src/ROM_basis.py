def convert_to_linux(path: str) -> str:
    """
    Helper function to convert Windows paths to Linux paths for WSL compatibility. If the path is already in Linux format, it is returned unchanged.

    Arguments:
        path: sting representing the file path to be converted.

    Returns:
        A string representing the converted file path in Linux format.

    """
    if platform.system() == "Windows":
        path = path.replace("\\", "/")
        if path[1] == ":":
            drive = path[0].lower()
            rest = path[2:]
            rest = rest.lstrip("/")
            return f"/mnt/{drive}/{rest}"
        else:
            return path
    else:
        return path