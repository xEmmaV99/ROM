class ROM_basis:
    """
    Class to represent the ROM_basis

    Attributes:
        omegas: The frequencies (omegas) at which the snapshots were taken, a 1D array (num_snapshots,) of complex values.
        snapshots: The snapshot data, typically a 3D array (num_snapshots, 2, entries), first dimension refers to the snapshot frequency, second dimension is for X and Y and the third dimension contains the matrix elements.
        F: The F vector associated with the snapshots (stacked F20, F02)
    """
    def __init__(self):
        """
        Initialise the ROM_basis with empty attributes. The actual data will be loaded using the load method.
        """
        self.omegas = None
        self.snapshots = None # todo consider merging omegas and snapshots together ?
        self.F = None

    def is_loaded(self):
        return self.omegas is not None and self.snapshots is not None


    def load(self, path_to_snapshot):
        """
        Loads the basis from a given snapshot file

        Args:
            path_to_snapshot: The file path to the snapshot file generated by the FAM runs, which contains the frequencies (omegas), snapshots and F vector.
        """
        from src.Utils_parsers import parse_XY_numba
        # load snapshots and omegas from file
        self.snapshots, self.omegas, self.F = parse_XY_numba(path_to_snapshot)
        return self

class data:
    """
    Class to represent the data parsed from the mean-field output file, as well as the FAM parameters.

    Attributes:
        num_proton: Number of protons in the nucleus.
        num_neutron: Number of neutrons in the nucleus.
        num_proton_wf: Number of proton wavefunctions.
        num_neutron_wf: Number of neutron wavefunctions.
        param: The parameterization used in the mean-field calculation.
        dx: The mesh spacing used in the mean-field calculation.
        size: The size of the mesh used in the mean-field calculation.
        w_min: Minimum frequency for the strength (default 0.0 MeV).
        w_max: Maximum frequency for the strength (default 20.0 MeV).
        smear: Smearing parameter (default 1.0 MeV) often called 'target smearing'.
        l: Multipolarity l of F (default 0).
        m: Multipolarity m of F (default 0).
        param_prefix: A dictionary mapping parameterization names to their prefixes used in Tantalus executables.

    """
    def __init__(self, file, fam_params=None):
        """
        Initialize the data object by parsing the mean-field output file and extracting the relevant parameters

        Attributes:
            file:  The file path to the mean-field output file, which contains the information about the nucleus and the mean-field calculation parameters.
            fam_params:  A dictionary containing the FAM parameters (w_min, w_max, smear, l, m). If None, default values will be used.
        """

        parse = self._parse_mf(file)
        self.num_proton = parse["num_proton"]
        self.num_neutron = parse["num_neutron"]
        self.num_proton_wf = parse["num_proton_wf"]
        self.num_neutron_wf = parse["num_neutron_wf"]
        self.param = parse["param"]
        self.dx = parse["dx"]
        self.size = parse["size"]

        self.param_prefix = {"BSkG2": "BXL",
                             "BSkG4": "BXL",
                             "BSkG5": "BXL-N2LO"}  # dict for parameterization prefixes @ tantalus specific

        self._fam_defaults = {
            'w_min': 0.0,
            'w_max': 20.0,
            'smear': 1.0,
            'l': 0,
            'm': 0
        }
        self.set_FAM_parameters(fam_params) # extract FAM parameters

    def set_FAM_parameters(self, fam_params):
        # FAM data
        if fam_params is None:
            fam_params = {}
        params = self._fam_defaults.copy()
        params.update(fam_params)
        # Set the attributes
        for key, value in params.items():
            setattr(self, key, value)

    def _parse_mf(self, file: str):
        # change linux path to windows path
        text = Path(file.replace("/mnt/c/", "C:/")).read_text()

        match = re.search(r"General Information\s*-+\s*(.*)", text, re.DOTALL)
        if match:
            text = match.group(1) # check below General Information

        def grab(pattern, cast):
            m = re.search(pattern, text)
            if not m:
                raise ValueError(f"Could not find pattern: {pattern}")
            return cast(m.group(1))

        return {
            "num_neutron": grab(r"N =\s*([0-9]+)", int),
            "num_proton": grab(r"Z =\s*([0-9]+)", int),
            "num_neutron_wf": grab(r"nwn\s*=\s*(\d+)", int),
            "num_proton_wf": grab(r"nwp\s*=\s*(\d+)", int),
            "param": grab(r"Parameterization name\s+(\S+)", str),
            "dx": grab(r'dx\s*=\s*([0-9.eE+-]+)', float),
            "size": grab(r'nx\s*=\s*([0-9]+)', float) * grab(r'dx\s*=\s*([0-9.eE+-]+)', float),
        }

    @property
    def prefix(self):
        return self.param_prefix[self.param]

class ROM_builder:
    """
    Class to build the ROM basis by launching FAM runs and processing the output. Can be omitted if FAM calculations are dealt with externally

    Attributes:
        data: An instance of the data class containing the mean-field and FAM parameters.
        basis: An instance of the ROM_basis class to store the snapshots and omegas.
        working_directory: The directory where FAM runs are launched and intermediate files are stored.
        TANTALUS_PATH: The path to the Tantalus installation, used for launching FAM runs.
        build_type: The method used to build the ROM basis. Can be "equidistant_1D", "greedy", "greedy_2D" or "contour".
        run_type: The method used to run the FAM calculations. Can be "generate_runfiles" to only generate the runfiles without executing them, or "default" to execute the runs immediately after generating the runfiles.
        max_num_snapshots: Maximum number of snapshots included in the basis. The iterative methods will stop once this number is reached
        path_to_meanfield_wf: The path to the mean-field wavefunction file, which is used as input for the FAM runs.
        path_to_snapshot: The path to the snapshot file generated by the FAM runs, which is used to load the ROM basis after building.
        output_dir: The directory where the processed FAM output (snapshots and omegas) is stored after merging.
        greedy_settings: A dictionary containing settings specific to the greedy build type, such as convergence criteria for selecting new snapshots.
        greedy_2D_settings: A dictionary containing settings specific to the greedy_2D build type, such as convergence criteria, threshold and maximum smearing to consider for new snapshots in the 2D greedy method.

    """
    def __init__(self,
                 path_to_meanfield_wf: str,
                 path_to_meanfield_out: str,
                 FAM,
                 tantalus_path="$HOME/code/tantalus/"):
        """
        Class to set up the ROM builder which handles the entire process of building the ROM basis

        Args:
            path_to_meanfield_wf: path to the mean-field wavefunction file
            path_to_meanfield_out: path to the mean-field output file (required to extract relevant mean-field parameters)
            FAM: a dictionary containing the FAM parameters (w_min, w_max, smear, l, m, ...). If None, default values will be used.
            tantalus_path: path to the Tantalus installation
        """
        self._DEBUG_clear_wd = True # if False, the output files are not merged and the working dir is not cleared. Only used for debugging purposes
        self._build_types = ['equidistant_1D', 'greedy', 'greedy_2D','contour']
        self._run_types = ['generate_runfiles', 'default']

        self.data = data(path_to_meanfield_out)
        self.data.set_FAM_parameters(FAM)

        #DEBUG
        if "max_num_snapshots" in FAM.keys():
            print("Max num snapshots not used! Setting it by writing to ROM_builder.max_num_snapshots!")

        self.max_num_snapshots = 5
        self.build_type = 'equidistant_1D'
        self.path_to_meanfield_wf = convert_to_linux(path_to_meanfield_wf)

        self.basis = ROM_basis() # to be loaded!!
        self.basis.load = self.load # overwrite function to have the same function but ALSO save path

        self.working_directory = self.get_base_dir().joinpath('work_dir')


        # check if Tantalus is installed, if not raise error
        self.set_TANTALUS_path(tantalus_path)

        # todo check if wf input file is valid

        self.path_to_snapshot = None # to be set
        self.run_type = 'default' # run all by default
        self.tmp_output = "TMP_SNAPSHOTS"
        self.output_dir = self.working_directory.parent.parent.joinpath("_outputs")

        self.greedy_settings = {"convergence_criterion": 0.0}
        self.greedy_2D_settings = {"convergence_criterion": 0.0, #threshold of the greedy snapshot cost cutoff
                                   "threshold": 0.1, #selecting snapshots below 0.1 relative cost
                                   "max_smearing": 20.0, } # maximum smearing to consider for new snapshots in 2D greedy


    def get_base_dir(self):
        return Path(__file__).resolve().parent

    def set_TANTALUS_path(self, path):
        """
        Overrides Tantalus path and checks for validity

        Args:
            path: string to the Tantalus installation
        """
        self.TANTALUS_PATH = path
        print(f"Checking Tantalus installation at: {self.TANTALUS_PATH}")
        if not self._check_tantalus_installation():
             raise Exception("Tantalus installation not found. No FAM runs can be launched.")


    def set_build_type(self, build_type):
        """
        Sets the build type for ROM construction and checks for validity

        Args:
            build_type: string specifying the build type. Supported types are "equidistant_1D", "greedy", "greedy_2D" and "contour".
        """
        if build_type not in self._build_types:
            raise ValueError("Unknown build type. Supported types: " + ", ".join(self._build_types))
        self.build_type = build_type


    def set_run_type(self, run_type):
        """
        Sets the run type for ROM construction and checks for validity

        Args:
            run_type: string specifying the run type. Supported types are "generate_runfiles" to only generate the runfiles without executing them, or "default" to execute the runs immediately after generating the runfiles.
        """
        if run_type not in self._run_types:
            raise ValueError("Unknown build type. Supported types: " + ", ".join(self._run_types))
        if run_type == 'generate_runfiles' and self.build_type == 'greedy':
            raise ValueError("Cannot only generate runfiles for greedy build type, as it requires iterative runs.")
        self.run_type = run_type


    def set_contour_parameters(self, n=13, R=500, r=0.1):
        """
        Sets the parameters for contour sampling. Only relevant if build_type is set to "contour".

        Args:
            n: the number of samples along the contour
            R: the outer radius of the contour
            r: the inner radius of the contour
        """
        self.contour_n = n
        self.contour_R = R
        self.contour_r = r


    def _clear_working_directory(self):
        if self.working_directory.exists():
            for item in self.working_directory.iterdir():
                if item.is_dir():
                    for subitem in item.iterdir():
                        subitem.unlink()
                    item.rmdir()
                else:
                    item.unlink()
        # make sure the working directory exists
        self.working_directory.mkdir(parents=True, exist_ok=True)


